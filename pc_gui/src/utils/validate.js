/**
 * @param {string} path
 * @returns {Boolean}
 */
export function isExternal(path) {
  return /^(https?:|mailto:|tel:)/.test(path)
}

/**
 * @param {string} str
 * @returns {Boolean}
 */
export function validPhoneNumber(str) {
  const regex = /^1[3-9]\d{9}$/;
  return regex.test(str);
}
